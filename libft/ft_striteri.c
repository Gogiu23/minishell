#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i;
	char	*p;

	i = 0;
	while (s[i])
	{
		p = &s[i];
		(*f)(i, p);
		i++;
	}
}
